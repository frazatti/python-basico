numeros = [5, 10, 9, 98, 20]

tamanho = len(numeros) # tamanho (length)
some = sum(numeros) # soma (sum)
minimo = min(numeros) # menor valor (min)
maximo = max(numeros) # maior valor (max)

# Imprimindo Valores
print(f"{numeros}")
print(f"tamanho = {tamanho}")
print(f"some = {some}")
print(f"minimo = {minimo}")
print(f"maximo = {maximo}")
